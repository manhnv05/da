     private void startCountdown() {
        if (timer != null && timer.isRunning()) {
            timer.stop();
        }
        countDownSeconds = 5 * 60; // reset về 5 phút
        updateTimeLabel();

        timer = new Timer(1000, e -> {
            countDownSeconds--;
            updateTimeLabel();
            if (countDownSeconds <= 0) {
                timer.stop();
                lblTime.setText("00:00");
                JOptionPane.showMessageDialog(this, "Mã OTP đã hết hạn! Vui lòng bấm gửi lại mã.");
            }
        });
        timer.start();
    }

    private void updateTimeLabel() {
        int minutes = countDownSeconds / 60;
        int seconds = countDownSeconds % 60;
        lblTime.setText(String.format("%02d:%02d", minutes, seconds));
    }
    
    private void disableResendOtp() {
        canResendOtp = false;
        resendCountdown = 60;
        lblGuiLaiOTP.setEnabled(false);
        lblGuiLaiOTP.setText("Gửi lại mã (" + resendCountdown + "s)");
        // Dừng timer cũ nếu có
        if (resendTimer != null && resendTimer.isRunning()) {
            resendTimer.stop();
        }
        resendTimer = new Timer(1000, e -> {
            resendCountdown--;
            lblGuiLaiOTP.setText("Gửi lại mã (" + resendCountdown + "s)");
            if (resendCountdown <= 0) {
                resendTimer.stop();
                enableResendOtp();
            }
        });
        resendTimer.start();
    }

    // Gọi hàm này khi cho phép gửi lại OTP
    private void enableResendOtp() {
        canResendOtp = true;
        lblGuiLaiOTP.setEnabled(true);
        lblGuiLaiOTP.setText("Chưa nhận được mã OTP?");
        if (resendTimer != null && resendTimer.isRunning()) {
            resendTimer.stop();
        }
    }
