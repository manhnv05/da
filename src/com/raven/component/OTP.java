    private void btnContinueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinueActionPerformed
        String inputOtp = txtOTP.getText().trim();
        if (inputOtp.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã xác thực!");
            txtOTP.requestFocus();
            return;
        }
        // Kiểm tra hết hạn
        long now = System.currentTimeMillis();
        if (now - otpCreatedTime > 5 * 60 * 1000) { // lớn hơn 5 phút
            JOptionPane.showMessageDialog(this, "Mã OTP đã hết hạn! Vui lòng bấm gửi lại mã.");
            return;
        }
        if (!inputOtp.equals(otpCode)) {
            JOptionPane.showMessageDialog(this, "Mã xác thực không đúng!");
            txtOTP.requestFocus();
            return;
        }
        // Nếu đúng, mở form đổi mật khẩu, truyền email sang
        JOptionPane.showMessageDialog(this, "Xác thực thành công! Bạn có thể đặt lại mật khẩu.");
        passnew changePassForm = new passnew(email); // Đảm bảo đã có constructor passnew(String email)
        changePassForm.setLocationRelativeTo(null);
        changePassForm.setVisible(true);
        this.dispose(); // đóng form OTP
    }//GEN-LAST:event_btnContinueActionPerformed

